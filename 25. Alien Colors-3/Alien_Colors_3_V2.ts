// Using switch statements.
alienColor = "yellow"; 

switch (alienColor) {
    case "green":
        console.log("The player earned 5 points.");
        break;
        case "yellow":
         console.log("The player earned 10 points.");
         break;
         case "red":
            console.log("The player earned 15 points.");
            break;
            default:
                console.log("Unknown alien Color");
}               
