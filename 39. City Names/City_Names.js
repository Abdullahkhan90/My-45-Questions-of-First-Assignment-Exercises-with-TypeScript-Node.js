// Function for city_country.
function city_country(city, country) {
    return `${city},${country}`;
}
// Results.
console.log(city_country("Lahore", "Pakistan"));
console.log(city_country("New Delhi", "India"));
console.log(city_country("Jeddah", "Saudia Arabia"));
